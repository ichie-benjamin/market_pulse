export function getAllAllowedAssets(): string[] {
    return Object.values(ALLOWED_ASSETS).flatMap(category =>
        category.map(asset => asset.name)
    );
}

export function getAssetDisplayName(assetName: string): string {
    for (const category of Object.values(ALLOWED_ASSETS)) {
        const asset = category.find(a => a.name === assetName);
        if (asset) {
            return asset.displayName || assetName;
        }
    }
    return assetName;
}


export function getAssetTradingViewSymbol(assetName: string): string | null {
    for (const category of Object.values(ALLOWED_ASSETS)) {
        const asset = category.find(a => a.name === assetName);
        if (asset) {
            return asset.tv_sym;
        }
    }
    return null;
}
